<template>
    <div class="text_group">
        <!-- 组件结构 -->
        <!-- 组件容器              error存在时添加is-invalid样式-->
        <div class="input_group" :class="{'is-invalid': error}">
            <!-- 输入框 -->
            <input 
                :type="type" 
                :placeholder="placeholder" 
                :value="value" 
                :name="name"
                @input="$emit('input',$event.target.value)"
             >
             <!-- 输入框后面的点击按钮 -->
             <button v-if="btnTitle" :disabled="disabled" @click="$emit('btnClick')">{{btnTitle}}</button>
        </div>
        <!-- 错误提醒 -->
        <div  v-if="error" class="invalid-feedback">{{error}}</div>
    </div>
</template>

<script>
export default {
    name:"inputGroup",
    // 属性注册实现参数传递：
    // type:input框类型,value: 值类型，placeholder: 提醒 ， btnTitle:验证码点击按钮 ， disable:是否禁止点击  error:错误提醒
    props: {
        type: {
            type: String,
            default: "text"
        },
        value:String,
        placeholder:String,
        name: String,
        btnTitle: String,
        disabled: Boolean,
        error: String
    }
};
</script>
